public class asdsa {

    public static void main(String[] args) {
        //     杨辉三角形
        int[][] arr = new int[10][10];
        for (int i = 0; i < arr.length; i++) {
            for (int j = 0; j < arr.length - 1; j++) {
                System.out.print(j +" ");
            }
        }

    }

}
